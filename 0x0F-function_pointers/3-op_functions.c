#include <stdlib.h>
#include "3-calc.h"
/**
*op_add - add two numbers
*@a: number a
*@b: number b
*Return: a + b
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
*op_sub - subtrates two numbers
*@a: number a
*@b: number b
*Return: a - b
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
*op_mul - multiply two numbers
*@a: number a
*@b: number b
*Return: a * b
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
*op_div - divide two numbers
*@a: number a
*@b: number b
*Return: a / b
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
*op_mod - module of two numbers
*@a: number a
*@b: number b
*Return: a % b
*/
int op_mod(int a, int b)
{
	return (a % b);
}
